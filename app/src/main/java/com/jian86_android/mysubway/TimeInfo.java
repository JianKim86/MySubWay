package com.jian86_android.mysubway;

public class TimeInfo {
    private String upper_time ;
    private String lower_time;
    private String time ;
    private String upper_Dstation;
    private String lower_Dstation;

    public String getUpper_time() {
        return upper_time;
    }

    public void setUpper_time(String upper_time) {
        this.upper_time = upper_time;
    }

    public String getLower_time() {
        return lower_time;
    }

    public void setLower_time(String lower_time) {
        this.lower_time = lower_time;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getUpper_Dstation() {
        return upper_Dstation;
    }

    public void setUpper_Dstation(String upper_Dstation) {
        this.upper_Dstation = upper_Dstation;
    }

    public String getLower_Dstation() {
        return lower_Dstation;
    }

    public void setLower_Dstation(String lower_Dstation) {
        this.lower_Dstation = lower_Dstation;
    }
}
